/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode(int x) : val(x), next(NULL) {}
 * };
 */
//官方，要判断真的是那个点，所以这个最合适。
class Solution
{
public:
    bool hasCycle(ListNode *head)
    {
        unordered_set<ListNode *> seen;
        while (head != nullptr)
        {
            if (seen.count(head))
            {
                return true;
            }
            seen.insert(head);
            head = head->next;
        }
        return false;
    }
};

